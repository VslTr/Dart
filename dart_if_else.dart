void if_else() {
  var a = 11, b = 2;
  if (a % b == 0 && b / a > 0) {
    print('Yes a % b = ${a % b}');
  } else if (a % b != 0 && b / a > 0) {
    print('No a % b != 0, = ${a % b}');
  } else {
    print('Error!!!');
  }
}

