void switch_case() {
  var digit = 6;
  switch (digit) {
    case 4:
      print('Namber Equal: 4');
      break;
    case 5:
      print('Namber Equal: 5');
      break;
    case 6:
      print('Namber Equal: 6');
      break;
    case 7:
      print('Namber Equal: 7');
      break;
    default: // не обязательно, только если необходимо что то выполнить когда все кейсы не сработали (типа 'else')
      print('Namber Unknown !!!');
  }
}
